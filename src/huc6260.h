    void SaveState(std::ostream& stream);
    void LoadState(std::istream& stream);

private:
    void InitPalettes();
    void AdjustForMultipleDividers();
    template <bool is_sgx>
    void RenderFrame();
    template <bool SGX, int BPP>
    void RenderFrameTemplate();
    void CalculateScreenBounds();
